package aulas.aula08.bibliotecademetodos;

public class Unidades {
  public static double polegadasParaCentimetros(double pol) {
    return pol * 2.54;
  }

  public static double pesParaCentimetros(double pes) {
    return pes * 30.48;
  }

  public static double milhasParaQuilometros(double milhas) {
    return milhas * 1.609;
  }
}
